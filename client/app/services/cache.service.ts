import { Injectable } from '@angular/core'
import { ResPostData } from '../../output'

@Injectable({
  providedIn: 'root'
})
export class CacheService {
  private posts?: ResPostData[]

  public setPosts(posts: ResPostData[]) {
    this.posts = posts
  }

  public getPosts() {
    return this.posts
  }

  public clearPosts() {
    this.posts = undefined
  }
}
